class DefaultAttributes {
String fname = "Susma";
String lname = "Sureshkumar";
String email = "suresh7sumathi@gmail.com";
int age = 20;
public static void main(String[] args) {
DefaultAttributes objDefault = new DefaultAttributes();
System.out.println("Name : " + objDefault.fname + " " + objDefault.lname);
System.out.println("Email : " + objDefault.email);
System.out.println("Age : " + objDefault.age);
}
}